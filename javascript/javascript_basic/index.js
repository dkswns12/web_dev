// 
// 변수는 let 선언 후, 해당 값을 변수에 대입-변동 가능
// 상수는 const로 선언 후, 해당 값을 바꿀수 없음
// 선언은 변수와 상수와 동일하게 한번만 선언
let value=1;
console.log(value);
value=2;
console.log(value);

const a=1;
console.log(a);

// 문자열은 작은 따옴표나 큰 따옴표 둘 다 가능
// 세미 콜론 붙이거나 안 붙일 수 있음
let text='hellow';
let text2="hellow";

// boolean 형태의 변수-true or false
// null은 존재 하지 않음을 의미하나 undefined는 변수를 선언만 하고 값을 설정 하지 않음을 의미
let success=true;
let disable=false;

let no=null;
let something;
console.log(something);


